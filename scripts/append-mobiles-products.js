const productsPath = path.join(__dirname, "..", "docs", "collections", "products", "products.json");
const existing = JSON.parse(fs.readFileSync(productsPath, "utf8"));
if (!Array.isArray(existing)) {
  throw new Error("products.json must be a JSON array");
}

const existingIds = new Set(existing.map((p) => p.id));
const mobiles = buildCategoryBatch(mobilesRaw);
for (const p of mobiles) {
  if (existingIds.has(p.id)) {
    throw new Error(`Duplicate id ${p.id} — already in products.json`);
  }
}

const merged = [...existing, ...mobiles];
fs.writeFileSync(productsPath, `${JSON.stringify(merged, null, 2)}\n`);

